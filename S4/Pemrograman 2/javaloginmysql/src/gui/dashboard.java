    void updateTables() {
        String pwd = new String(inp_pwd.getPassword());
        String confirmPwd = new String(inp_confirmpwd.getPassword());
        String ph = inp_numb.getText();
        String gender = "";
        String fullname = inp_name.getText();
        String email = inp_email.getText();
        if (cb_pria.isSelected()) {
            gender = "Male";
        }
        if (cb_wanita.isSelected()) {
            gender = "Female";
        }
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
        String birth = dateFormat.format(inp_date.getDate());
        String sql = "UPDATE REGISTER SET"
                + " fullname='" + fullname + "',email='" + email + "', "
                + " pwd='" + pwd + "',confirmpwd='" + confirmPwd + "',"
                + " phone='" + ph + "',gender='" + gender + "',birth=STR_TO_DATE('" + birth + "','%d-%m-%Y')"
                + " WHERE id=" + id;
        System.out.println("SQL : " + sql);
        con.dmlData(sql);
        displaytable();
    }

