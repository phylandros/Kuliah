    private void table_showMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_showMouseClicked
        // TODO add your handling code here:
        try {
            DefaultTableModel dt = (DefaultTableModel) table_show.getModel();
            int rowId = table_show.getSelectedRow();
            id = dt.getValueAt(rowId, 0).toString();
            String sql = "select * from register where id=" + id + "";
            System.out.println("SQL : " + sql);
            rs = con.lihatData(sql);
            while (rs.next()) {
                inp_name.setText(rs.getString("fullname"));
                inp_email.setText(rs.getString("email"));
                inp_pwd.setText(rs.getString("pwd"));
                inp_confirmpwd.setText(rs.getString("pwd"));
                inp_numb.setText(rs.getString("phone"));
                String gender = rs.getString("gender");
                if (gender.equals("Male")) {
                    cb_pria.setSelected(true);
                }
                if (gender.equals("Female")) {
                    cb_wanita.setSelected(true);
                }
                Date date = (Date) new SimpleDateFormat("yyyy-MM-dd").parse((String) rs.getString("birth"));
                System.out.println("Date : " + date);
                inp_date.setDate(date);
                bt_edit.setEnabled(true);
                bt_delete.setEnabled(true);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error : " + e);
        }
    }//GEN-LAST:event_table_showMouseClicked

