    private void tabel_show_empMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_show_empMouseClicked
        // TODO add your handling code here:
        displayTable();
        try {
            dt = (DefaultTableModel) jTable1.getModel();
            int rowId = jTable1.getSelectedRow();
            empId = dt.getValueAt(rowId, 0).toString();
            String sql = "SELECT e.id, e.employee_id,e.nama,e.alamat,e.tempat_lahir,e.tanggal_lahir,d.id deptid,d.deptcode\n"
                    + "FROM employees e\n"
                    + "INNER JOIN departement d\n"
                    + "on e.dept_id=d.id"
                    + " WHERE e.id=" + empId;
            System.out.println("SQL : " + sql);
            rs = con.lihatData(sql);
            while (rs.next()) {
                inp_emid.setText(rs.getString("employee_id"));
                inp_nama.setText(rs.getString("nama"));
                inp_tmp.setText(rs.getString("tempat_lahir"));
                inp_alamat.setText(rs.getString("alamat"));
                Date date = (Date) new SimpleDateFormat("yyyy-MM-dd").parse((String) rs.getString("tanggal_lahir"));
                System.out.println("Date : " + date);
                inp_tgl.setDate(date);
                String Txtcmb = rs.getString("deptcode").trim();
                int idCmb = rs.getInt("deptid");
                Item comboItem = new Item(idCmb, Txtcmb);
                cb_dept.setSelectedItem(comboItem);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error : " + e);
        }
    }//GEN-LAST:event_tabel_show_empMouseClicked

