    class Item {

        private int id;
        private String description;

        public Item(int id, String description) {
            this.id = id;
            this.description = description;
        }

        public int getId() {
            return id;
        }

        public String toString() {
            return description;
        }
    }
    
    public void addDept(JComboBox cmb) {

        cmb.removeAllItems();
        try {
            cmb.removeAllItems();
            cmb.addItem("-- Pilih Dept --");
            String query = "SELECT * FROM departement";
            rs = con.lihatData(query);
            while (rs.next()) {
                String Txtcmb = rs.getString("deptcode").trim();
                int idCmb = rs.getInt("id");
                Item comboItem = new Item(idCmb, Txtcmb);
                cmb.addItem(comboItem);
            }
        } catch (Exception e) {
            System.out.println("Error " + e);
        }
    }
    
