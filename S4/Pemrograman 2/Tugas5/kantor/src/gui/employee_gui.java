    void removeTable(JTable jt) {

        dt = (DefaultTableModel) tabel_show_emp.getModel();
        int rowCount = dt.getRowCount();
        for (int i = 0; i < rowCount; i++) {
            dt.removeRow(0);
        }
    }
    
    void displayTable() {

        removeTable(tabel_show_emp);
        String sql = "SELECT e.id, e.employee_id,e.nama,e.alamat,e.tempat_lahir,"
                + "e.tanggal_lahir,d.deptcode\n"
                + "FROM employees e\n"
                + "INNER JOIN departement d\n"
                + "on e.dept_id=d.id;";
        System.out.println("SQL : " + sql);
        rs = con.lihatData(sql);
        DefaultTableModel dt = (DefaultTableModel) tabel_show_emp.getModel();
        Object rows[];
        try {
            while (rs.next()) {
                int id = Integer.parseInt(rs.getString("id"));
                String empId = rs.getString("employee_id");
                String fullName = rs.getString("nama");
                String alamat = rs.getString("alamat");
                String tempatLahir = rs.getString("tempat_lahir");
                String birth = rs.getString("tanggal_lahir");
                String deptname = rs.getString("deptcode");
                
                rows = new Object[]{id, empId, fullName, alamat, tempatLahir, birth, deptname};
                dt.addRow(rows);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error : " + e);
        }

    }
   
