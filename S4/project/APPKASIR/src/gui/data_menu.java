    private void autonumber(){
        try {
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM data_menu ORDER BY id_menu DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String id_menu = r.getString("id_menu").substring(2);
                String MN = "" +(Integer.parseInt(id_menu)+1);
                String Nol = "";
                
                if (MN.length() == 1) 
                    {Nol = "00";}
                else if(MN.length()==2)
                    {Nol = "0";}
                else if(MN.length()==3)
                    {Nol = "";}
                
                
                txt_idmenu.setText("MN" + Nol + MN);  
            }else{
                txt_idmenu.setText("MN001");
            }
            r.close();
            s.close();
        } catch (Exception e) {
            System.out.println("autonumber error");
        }
    }
    
