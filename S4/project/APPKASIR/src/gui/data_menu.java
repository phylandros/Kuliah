    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
        int i = tabel_data.getSelectedRow();
        if (i == -1) {
            return;
        }
        String id = (String) model.getValueAt(i, 0);
        String nama = txt_namamenu.getText();
        String jenis = (String)cb_jenismenu.getSelectedItem();
        String harga = txt_hargamenu.getText();
                
        try {
            Connection c = koneksi.getKoneksi();
            String sql = "UPDATE data_menu SET nama_menu = ?, jenis_menu = ?, harga_menu = ? WHERE id_menu = ?";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, nama);
            p.setString(2, jenis);
            p.setString(3, harga);
            p.setString(4, id);
            
            p.executeUpdate();
            p.close();
            JOptionPane.showMessageDialog(null, "Data Terubah");
            btn_simpan.setEnabled(true);
            btn_edit.setEnabled(false);
            btn_hapus.setEnabled(false);
            btn_batal.setEnabled(false);
            clear();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Update Error");
            System.out.println("update error");
        }finally{
            loadData();
            autonumber();
        }
        
    }//GEN-LAST:event_btn_editActionPerformed

